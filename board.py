def drawFrame(snake: list[tuple], apple: tuple, emptyFrame = emptyFrame):
    newFrame: str = ''
    newTile: str = ''
    
    row = "`"
    col = 0
    for tile in emptyFrame:
        isApple = False
        isSnake = False
        if tile == '\n':
            letterAsNum = ord(row)
            letterAsNum += 1
            row = chr(letterAsNum)
            col = 0
            newTile = tile
        else:
            # if statements in place of elif/else because if an apple and snake are in the same position
            # I want the snake to be printed over the apple.
            if (row, col) == apple:
                newTile = "游린"
                isApple = True
            if (row, col) in snake:
                newTile = "游릴"
                isSnake = True
            if not(isApple or isSnake):
                newTile = tile
            col += 1
        newFrame += newTile
    return newFrame

